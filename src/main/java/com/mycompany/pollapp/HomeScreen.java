    private void bcreatepollActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bcreatepollActionPerformed
        if(noofqs == 0){
            JOptionPane.showMessageDialog(this, "Please select the no of options!!!",
               "Warning", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(tques.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please type the question!!!",
               "Warning", JOptionPane.WARNING_MESSAGE);
                return;
        }
        for(int i=0;i<noofqs;i++){
            if(toptions[i].getText().equals("")){
                JOptionPane.showMessageDialog(this, "Please fill all the options!!!",
               "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }
        try {
            String str = "insert into tbl_poll(poll_question, poll_type, password, status) values('"+tques.getText()+"',  \"\",\"\","+1+")";
            out.writeUTF("poll_insert");
            out.writeUTF(str);
            poll_id = in.readInt();
            for(int i=0;i<noofqs;i++){
                str = "insert into tbl_options(poll_id, option_value) values('"+poll_id+"', '"+toptions[i].getText()+"')";
                out.writeUTF("insert");
                out.writeUTF(str);
            }
            JOptionPane.showMessageDialog(this, "Created Succesfully!!!");
            optionpane.removeAll();
            optionpane.revalidate();
            optionpane.repaint();
            noofqs = 0;
            tques.setText("");
            noofitems.setSelectedIndex(-1);
            
            
            
        } catch (IOException ex) {
            Logger.getLogger(HomeScreen.class.getName()).log(Level.SEVERE, null, ex);
            
        }
        catch (Exception e) {
            System.out.println("Ex is"+e);
        
        }
        
        
// TODO add your handling code here:
    }//GEN-LAST:event_bcreatepollActionPerformed


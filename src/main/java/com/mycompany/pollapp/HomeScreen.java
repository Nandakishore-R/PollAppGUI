    private void noofitemsItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_noofitemsItemStateChanged
        noofqs = Integer.parseInt(String.valueOf(noofitems.getSelectedItem()));
        optionpane.removeAll();
        optionpane.repaint();
        toptions = new JTextField[noofqs];
        loptions = new JLabel[noofqs];
        int y = 30;
        for(int i=0;i<noofqs;i++){
            loptions[i] = new JLabel("Option "+(i+1));
            toptions[i] = new JTextField();
            optionpane.add(loptions[i]);
            optionpane.add(toptions[i]);
            loptions[i].setBounds(50, y, 70, 16);
            toptions[i].setBounds(220, y, 90, 22);

            y = y+30;
        }
        optionpane.revalidate();
        optionpane.repaint();
        // TODO add your handling code here:
    }//GEN-LAST:event_noofitemsItemStateChanged

